installed_dir=$(dirname $(readlink -f $(basename `pwd`)))
##################################################################################################################
echo "################################################################"
echo "################### Autostart certain applications"
echo "################################################################"
echo

[ -d $HOME"/.config/autostart" ] || mkdir -p $HOME"/.config/autostart"
sleep 1
cp -f $installed_dir/settings/autostart/* $HOME"/.config/autostart"

echo "################################################################"
echo "################### Done"
echo "################################################################"

